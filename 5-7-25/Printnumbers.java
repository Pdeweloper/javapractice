//print 1-5 using ++i

class PrintOnetoFive
{
public static void main(String args[])    //it take 5 steps to print
{
int i=1;
System.out.println(i); //1
++i;
System.out.println(i); //2
++i;
System.out.println(i); //3
++i;
System.out.println(i); //4
++i;
System.out.println(i); //5

}
}

