class BallMove
{
	public static void main(String...x)
	{
		int mov = 0;
		
		System.out.print("Ball is moving up : ");
		++mov;
		System.out.print(mov);
		++mov;
		System.out.print(mov);
		++mov;
		System.out.print(mov);
		++mov;
		System.out.print(mov);
		++mov;
		System.out.print(mov);
		
		System.out.println();
		
		System.out.print("Ball is moving down : ");
		--mov;
		System.out.print(mov);
		--mov;
		System.out.print(mov);
		--mov;
		System.out.print(mov);
		--mov;
		System.out.print(mov);
		--mov;
		System.out.print(mov);
	}
}